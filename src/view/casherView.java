    public void getAllRecords()
    {
        try {
            DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ArrayList<loginModel> list=loginMethods.getAllRecords();
            Iterator<loginModel> itr=list.iterator();
            while(itr.hasNext())
            {
                loginModel log=itr.next();
                dtm.addRow(new Object[]
                {
                    log.getId(),
                    log.getName(),
                    log.getEmail(),
                    log.getUserType(),
                    log.getNotes()
                });
            }
        } catch (Exception e) {
        }
    }
