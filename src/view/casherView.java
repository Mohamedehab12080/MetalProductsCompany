    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
              ResultSet rs=dbOperations.getData("select * from login where email='"+txtEmail.getText()+"' OR name='"+txtCasherName.getText()+"'");
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"<html><h1>هذا الاسم موجود مسبقا</h1></html>");
            }else 
            {
                loginModel user=new loginModel();
            user.setName(txtCasherName.getText());
            user.setEmail(txtEmail.getText());
            user.setPassword(txtPassword.getText());
            user.setUserType(txttype.getSelectedItem().toString());
            user.setNotes(txtNote.getText());
            loginMethods.addUser(user);
            getAllRecords();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
          
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            loginModel user=new loginModel();
            user.setId(Integer.parseInt(txtID.getText()));
            user.setName(txtCasherName.getText());
            user.setEmail(txtEmail.getText());
            user.setPassword(txtPassword.getText());
            user.setUserType(txttype.getSelectedItem().toString());
            user.setNotes(txtNote.getText());
            loginMethods.updateUser(user);
            getAllRecords();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        getAllRecords();
    }//GEN-LAST:event_btnUpdateActionPerformed

