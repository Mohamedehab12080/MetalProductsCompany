    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int index = jTable1.getSelectedRow();
            TableModel tm = jTable1.getModel();
            txtID.setText(tm.getValueAt(index, 0).toString());
            txtCasherName.setText(tm.getValueAt(index, 1).toString());
            txttype.setSelectedItem(tm.getValueAt(index, 3).toString());
            txtEmail.setText(tm.getValueAt(index, 2).toString());
            txtNote.setText(tm.getValueAt(index, 4).toString());
            btnUpdate.setEnabled(true);
            ResultSet rs=dbOperations.getData("select password from login where id="+txtID.getText());
            if(rs.next())
            {
                txtPassword.setText(rs.getString("password"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

