
package model;

public class capenat {
    
    private int cabinetId;
    private double cabinetRealValue;
    private double editedValue;
    private double AfterEdited;
    private String operation;
    private String date;

    public int getCabinetId() {
        return cabinetId;
    }

    public void setCabinetId(int cabinetId) {
        this.cabinetId = cabinetId;
    }

    public double getCabinetRealValue() {
        return cabinetRealValue;
    }

    public void setCabinetRealValue(double cabinetRealValue) {
        this.cabinetRealValue = cabinetRealValue;
    }

    public double getEditedValue() {
        return editedValue;
    }

    public void setEditedValue(double editedValue) {
        this.editedValue = editedValue;
    }

    public double getAfterEdited() {
        return AfterEdited;
    }

    public void setAfterEdited(double AfterEdited) {
        this.AfterEdited = AfterEdited;
    }

    public String getOperation() {
        return operation;
    }

    public void setOperation(String operation) {
        this.operation = operation;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
}
